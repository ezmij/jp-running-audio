def write_to_sheet(rows, start_row=8, start_col="B"):
    """Write rows to the sheet using gws CLI."""
    # Convert to the 2D values array for the API
    # Each row is a list of cells for columns B-H (7 cols to cover practice questions)
    values = []
    for row in rows:
        # Pad to 7 columns (B through H)
        padded = list(row) + [""] * (7 - len(row))
        values.append(padded[:7])

    end_row = start_row + len(rows) - 1
    range_str = f"不安な脳!{start_col}{start_row}:H{end_row}"

    body = {
        "range": range_str,
        "values": values,
        "majorDimension": "ROWS"
    }

    cmd = [
        "gws", "sheets", "spreadsheets", "values", "update",
        "--params", json.dumps({
            "spreadsheetId": SHEET_ID,
            "range": range_str,
            "valueInputOption": "RAW"
        }),
        "--json", json.dumps(body)
    ]

    print(f"Writing {len(rows)} rows to {range_str} ...", flush=True)
    result = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
    if result.returncode != 0:
        print(f"ERROR: {result.stderr}", flush=True)
        return False
    print(f"OK: {result.stdout[:200]}", flush=True)
    return True


